$(document).ready(function () {
    $("#ma1").flipBook({
        pdfUrl: "pdf/ma1.pdf",
        viewMode: 'swipe',
        deeplinking: {
            enabled: true
        },
        skin: 'dark',
        layout: 4,
        btnSound: {
            vAlign: "top",
            hAlign: "left"
        },
        btnSelect:{
            enabled: false,
        },
        btnBookmark:{
            enabled: false,
        },
        btnToc:{
            enabled: false,
        },
        btnThumbs:{
            enabled: false,
        },
        btnAutoplay: {
            enabled: false,
        },
        currentPage: {
            vAlign: "bottom",
            hAlign: "left"
        },
        btnDownloadPages:{
            enabled: true,
            title: "Download pages",
            icon: "fa-download",
            icon2: "file_download",
            url: "pdf/ma1.pdf",
            name: "ma1.pdf"
        },
        btnDownloadPdf:{
            enabled: true,
            title: "Download PDF",
            icon: "fa-download",
            icon2: "picture_as_pdf",
            openInNewWindow: true,
            url: "pdf/ma1.pdf",
            name: "ma1.pdf"
        },
        btnClose:{
            title: "Close", 
            icon: "fa-times", 
            icon2: "close", 
            hAlign:'left', 
            vAlign:'top' 
        },
        btnPrint:{
            enabled: false,
        },
        btnExpand:{
            enabled: false,
        },
        lightBox: true,
        lightboxBackground: 'rgb(219,244,248)',
        // singlePageMode: true,
        viewMode: "3d",
        icons:'material',
        
    });

})
